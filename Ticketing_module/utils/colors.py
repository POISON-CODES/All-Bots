import discord

class BotColors():
       async def green(self):
              return discord.Color(0x00FF00)

       async def white(self):
              return discord.Color(0xFFFFFF)

       async def black(self):
              return discord.Color(0x000000)

       async def red(self):
              return discord.Color(0xFC0303)

       async def bg(self):
              return discord.Color(0x2F3136)

       async def orange(self):
              return discord.Color(0xFF6303)